    <h2>О нас</h2>
    <p class="info-block">
        Наша компания предоставляет услуги юридической консультации и поддержки в вопросах получения ВНЖ в Словакии.
        В отличие от многих других агентств, мы  осуществляем персональное обслуживание каждого клиента
        и обеспечиваем индивидуальный подход к каждой ситуации. Мы проявляем внимание именно к
        Вашим конкретным потребностям и запросам, помогая найти кратчайший и наиболее оптимальный путь к
        осуществлению Ваших планов.
    </p>
    <h3>Oсновные направления работы компании :</h3>
    <ul>
        <li>помощь в открытии бизнеса и оформлении ВНЖ</li>
        <li>помощь при поступлении в университеты Словакии</li>
        <li>сопровождение при обучении на курсах словацкого языка</li>
    </ul>
    <p class="info-block">

        Нашей целью является не только улучшение качества жизни своих клиентов,
        но и помощь в осуществлении их самых смелых планов и  целей. Мы напрямую сотрудничаем языковыми школами и
         государственными струкиурами.  За нашими плечами многолетний опыт работы в данной сфере.

    </p>

    <h3>Иностранным гражданам ВНЖ в Словакии оформляется на таких основаниях:</h3>
    <ul>
        <li>в случае ведения бизнеса на территории Словакии;</li>
        <li>во время обучения;</li>
        <li>в случае трудовой деятельности в государстве;</li>
        <li>воссоединение с семьей;</li>
        <li>служба в частях вооруженных сил на территории Словакии.</li>
        <li>
            Кроме того, ВНЖ возможен, если у претендента имеется синяя карточка Евросоюза.
            Статус заграничного словака также открывает приоритет для оформления ВНЖ в Словакии.
        </li>
    </ul>

    <h3>Преимущества ВНЖ в Словакии</h3>
    <ul>
        <li>Беспрепятственный въезд в государства Евросоюза и Шенгенского соглашения.</li>
        <li>Обучение, медицинское обслуживание в европейских странах.</li>
        <li>Получение виз для членов семейства в краткие сроки.</li>
        <li>Ведение предпринимательской деятельности на территории стран ЕС.</li>
        <li>Отсутствие конкретных сроков пребывания в Словакии после оформления ВНЖ.</li>
        <li>Кредитование на европейских условиях.</li>
        <li>Законодательство.</li>
    </ul>
    <p>
        ВНЖ можно получить согласно закону о проживании иностранных граждан на территории Словакии №48/2002,
        со всеми дополнениями и изменениями.
    </p>

    <h3>Преимущества для бизнесса</h3>
    <ul>
        <li>Предприниматели не указывают определенной суммы налогообложения.</li>
        <li>Контроль со стороны государства над работой фирмы не строгий, лояльный.</li>
        <li>Простой процесс оформления вида на жительство.</li>
        <li>Однако существуют определенные требования, позволяющие определить подлинность компании.</li>
